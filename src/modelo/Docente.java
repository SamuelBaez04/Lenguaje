package modelo;

/**
 *
 * @author samue
 */
public class Docente {
    
    private String nombre;
    private int id;
    private String profesion;
    private int edad;

    public Docente() {
    }

    public Docente(String nombre, int id, String profesion, int edad) {
        this.nombre = nombre;
        this.id = id;
        this.profesion = profesion;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getProfesion() {
        return profesion;
    }

    public void setProfesion(String profesion) {
        this.profesion = profesion;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
    
}
